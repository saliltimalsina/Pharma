// Deterministic bin code for newly created batches (matches the seed bin scheme).
export function deriveBin(warehouseId: string, seq: number): string {
  return `${warehouseId.replace('WH-', 'WH')}-A${(seq % 5) + 1}-R${(seq % 3) + 1}-S${(seq % 4) + 1}`;
}

// Opening movement ledger, reconstructed from the real seed records:
//   - one 'In' receipt per batch (receivedQty via its GRN)
//   - a 'Transfer' out/in pair for every COMPLETED transfer
//   - a signed 'Adjustment' for every APPROVED adjustment line
// Every quantity here traces to an existing seed field — nothing is invented.
// Further movements are appended live by the store as actions run this session.
let seedMovementSeq = 0;
function seedMovement(
  type: StockMovement['type'],
  itemId: string,
  batchNumber: string,
  warehouseId: string,
  qty: number,
  reference: string,
  by: string,
  date: string,
): StockMovement {
  seedMovementSeq += 1;
  return { id: `MOV-${String(seedMovementSeq).padStart(4, '0')}`, type, itemId, batchNumber, warehouseId, qty, reference, by, date };
}

export const movements: StockMovement[] = [
  ...batches.map((b) =>
    seedMovement(
      'In',
      b.itemId,
      b.batchNumber,
      b.warehouseId,
      b.receivedQty,
      b.grnNumber,
      b.approvedBy || b.supplierName,
      b.releasedDate || b.manufacturingDate,
    ),
  ),
  ...transfers
    .filter((t) => t.status === 'Completed')
    .flatMap((t) =>
      t.items.flatMap((li) => [
        seedMovement('Transfer', li.itemId, li.batchNumber, t.fromWarehouseId, -li.quantity, t.transferNumber, t.requestedBy, t.transferDate),
        seedMovement('Transfer', li.itemId, li.batchNumber, t.toWarehouseId, li.quantity, t.transferNumber, t.requestedBy, t.transferDate),
      ]),
    ),
  ...adjustments
    .filter((a) => a.status === 'Approved')
    .flatMap((a) =>
      a.items.map((li) =>
        seedMovement('Adjustment', li.itemId, li.batchNumber, a.warehouseId, li.actualQty - li.currentQty, a.adjustmentNo, a.approver, a.date),
      ),
    ),
].sort((a, b) => (a.date < b.date ? -1 : a.date > b.date ? 1 : a.id.localeCompare(b.id)));

export function itemById(id: string) {
  return items.find((i) => i.id === id);
}
export function warehouseById(id: string) {
  return warehouses.find((w) => w.id === id);
}
export function batchById(id: string) {
  return batches.find((b) => b.id === id);
}
export function stockById(id: string) {
  return stockEntries.find((s) => s.id === id);
}
export function transferById(id: string) {
  return transfers.find((t) => t.id === id);
}
export function adjustmentById(id: string) {
  return adjustments.find((a) => a.id === id);
}
